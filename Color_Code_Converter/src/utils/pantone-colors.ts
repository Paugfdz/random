// Función para calcular la distancia euclidiana entre dos colores en espacio RGB
export function colorDistance(
  rgb1: { r: number; g: number; b: number },
  rgb2: { r: number; g: number; b: number }
): number {
  const rDiff = rgb1.r - rgb2.r;
  const gDiff = rgb1.g - rgb2.g;
  const bDiff = rgb1.b - rgb2.b;
  return Math.sqrt(rDiff * rDiff + gDiff * gDiff + bDiff * bDiff);
}

// Función para encontrar el color Pantone más cercano
export function findClosestPantone(hex: string): PantoneColor {
  const rgb = hexToRgb(hex);
  
  let closestColor = pantoneColors[0];
  let minDistance = colorDistance(rgb, closestColor.rgb);
  
  for (const pantoneColor of pantoneColors) {
    const distance = colorDistance(rgb, pantoneColor.rgb);
    if (distance < minDistance) {
      minDistance = distance;
      closestColor = pantoneColor;
    }
  }
  
  return closestColor;
}

// Función para convertir HEX a RGB
export function hexToRgb(hex: string): { r: number; g: number; b: number } {
  const result = /^#?([a-f\d]{2})([a-f\d]{2})([a-f\d]{2})$/i.exec(hex);
  return result
    ? {
        r: parseInt(result[1], 16),
        g: parseInt(result[2], 16),
        b: parseInt(result[3], 16),
      }
    : { r: 0, g: 0, b: 0 };
}

// Función para convertir RGB a HEX
export function rgbToHex(r: number, g: number, b: number): string {
  return "#" + [r, g, b].map(x => {
    const hex = x.toString(16);
    return hex.length === 1 ? "0" + hex : hex;
  }).join("").toUpperCase();
}
